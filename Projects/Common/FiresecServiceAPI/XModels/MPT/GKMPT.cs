using System.Collections.Generic;
using System.Runtime.Serialization;
using System.Xml.Serialization;
using Common;

namespace FiresecAPI.GK
{
	/// <summary>
	/// Модуль пожаротушения ГК
	/// </summary>
	[DataContract]
	public class GKMPT : GKBase
	{
		public GKMPT()
		{
			StartLogic = new GKLogic();
			MPTDevices = new List<GKMPTDevice>();
			Delay = 10;
			Devices = new List<GKDevice>();

			HandAutomaticOffPim = new GKPim();
			HandAutomaticOffPim.IsAutoGenerated = true;
			HandAutomaticOffPim.MPTUID = UID;
			HandAutomaticOffPim.UID = GuidHelper.CreateOn(UID, 0);

			DoorAutomaticOffPim = new GKPim();
			DoorAutomaticOffPim.IsAutoGenerated = true;
			DoorAutomaticOffPim.MPTUID = UID;
			DoorAutomaticOffPim.UID = GuidHelper.CreateOn(UID, 1);

			FailureAutomaticOffPim = new GKPim();
			FailureAutomaticOffPim.IsAutoGenerated = true;
			FailureAutomaticOffPim.MPTUID = UID;
			FailureAutomaticOffPim.UID = GuidHelper.CreateOn(UID, 2);
		}

		bool _isLogicOnKau;
		[XmlIgnore]
		public override bool IsLogicOnKau
		{
			get { return _isLogicOnKau; }
			set
			{
				_isLogicOnKau = value;
				HandAutomaticOffPim.IsLogicOnKau = true;
				DoorAutomaticOffPim.IsLogicOnKau = true;
				FailureAutomaticOffPim.IsLogicOnKau = true;
			}
		}

		[XmlIgnore]
		public GKPim HandAutomaticOffPim { get; private set; }

		[XmlIgnore]
		public GKPim DoorAutomaticOffPim { get; private set; }

		[XmlIgnore]
		public GKPim FailureAutomaticOffPim { get; private set; }

		[XmlIgnore]
		public List<GKDevice> Devices { get; set; }

		/// <summary>
		/// Время задержки на включение
		/// </summary>
		[DataMember]
		public int Delay { get; set; }

		/// <summary>
		/// Время удержания
		/// </summary>
		[DataMember]
		public int Hold { get; set; }

		/// <summary>
		/// Режим после окончания удержания
		/// </summary>
		[DataMember]
		public DelayRegime DelayRegime { get; set; }

		/// <summary>
		/// Логика включения
		/// </summary>
		[DataMember]
		public GKLogic StartLogic { get; set; }

		/// <summary>
		/// Устройства МПТ
		/// </summary>
		[DataMember]
		public List<GKMPTDevice> MPTDevices { get; set; }

		/// <summary>
		/// Отключать автоматику при срабатывании датчика двери-окна
		/// </summary>
		[DataMember]
		public bool UseDoorAutomatic { get; set; }

		/// <summary>
		/// Останавливать при срабатывании датчика двери-окна
		/// </summary>
		[DataMember]
		public bool UseDoorStop { get; set; }

		/// <summary>
		/// Отключать автоматику при неисправности любого устройства
		/// </summary>
		[DataMember]
		public bool UseFailureAutomatic { get; set; }

		[XmlIgnore]
		public override GKBaseObjectType ObjectType { get { return GKBaseObjectType.MPT; } }

		[XmlIgnore]
		public override string PresentationName
		{
			get { return "MПТ" + "." + No + "." + Name; }
		}
	}
}