using System.Runtime.Serialization;
using System;
using System.Collections.Generic;

namespace XFiresecAPI
{
	[DataContract]
	public class XDelay : XBase, IInputObjectsBase, INamedBase
	{
		public XDelay()
		{
			//UID = BaseUID;
			DeviceLogic = new XDeviceLogic();

			InputDevices = new List<XDevice>();
			InputZones = new List<XZone>();
			InputDirections = new List<XDirection>();
		}

		public List<XDevice> InputDevices { get; set; }
		public List<XZone> InputZones { get; set; }
		public List<XDirection> InputDirections { get; set; }

		[DataMember]
		public int No { get; set; }

		[DataMember]
		public string Name { get; set; }

		[DataMember]
		public string Description { get; set; }

		[DataMember]
		public ushort DelayTime { get; set; }

		[DataMember]
		public ushort Hold { get; set; }

		[DataMember]
		public DelayRegime DelayRegime { get; set; }

		[DataMember]
		public XDeviceLogic DeviceLogic { get; set; }

		[DataMember]
		public bool IsAutoGenerated { get; set; }

		[DataMember]
		public Guid PumpStationUID { get; set; }

		public override XBaseObjectType ObjectType { get { return XBaseObjectType.Delay; } }

		public override string PresentationName
		{
			get { return Name; }
		}
	}
}